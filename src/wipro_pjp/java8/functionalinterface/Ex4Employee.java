package wipro_pjp.java8.functionalinterface;

public class Ex4Employee {
private int id;
private String name;
private double salary;
public int getId() {
	return id;
}
public String getName() {
	return name;
}
public double getSalary() {
	return salary;
}
public Ex4Employee() {
	
}
public Ex4Employee(int id, String name, double salary) {
	super();
	this.id = id;
	this.name = name;
	this.salary = salary;
}


}
